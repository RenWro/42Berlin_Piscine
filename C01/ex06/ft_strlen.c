#include <unistd.h>
#include <stdio.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	return (i);
}

int	main(void)
{
	int		len;
	char	*sent;

	sent = "Hello 42!";
	len = ft_strlen(sent);
	printf("%d", len);
	return (0);
}
